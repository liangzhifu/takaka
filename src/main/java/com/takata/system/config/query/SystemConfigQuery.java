package com.takata.system.config.query;

/**
 * @author lzf
 **/

public class SystemConfigQuery {

    private Integer start;

    private Integer size;

    private Integer configCodeId;

    public Integer getStart() {
        return start;
    }

    public void setStart(Integer start) {
        this.start = start;
    }

    public Integer getSize() {
        return size;
    }

    public void setSize(Integer size) {
        this.size = size;
    }

    public Integer getConfigCodeId() {
        return configCodeId;
    }

    public void setConfigCodeId(Integer configCodeId) {
        this.configCodeId = configCodeId;
    }
}
